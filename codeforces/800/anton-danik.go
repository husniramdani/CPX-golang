package main

import (
	"fmt"
	"strings"
)

func main() {
	var n int
	var s string
	fmt.Scan(&n, &s)
	a := strings.Count(s, "A")
	d := strings.Count(s, "D")
	if a > d {
		fmt.Println("Anton")
	} else if a < d {
		fmt.Println("Danik")
	} else {
		fmt.Println("Friendship")
	}
}
