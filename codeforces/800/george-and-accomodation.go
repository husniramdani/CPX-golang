package main

import "fmt"

func main() {
	var n, p, q int
	ans := 0
	fmt.Scan(&n)
	for i := 0; i < n; i++ {
		fmt.Scan(&p, &q)
		if q-p >= 2 {
			ans++
		}
	}
	fmt.Println(ans)
}
