package main

import (
	"fmt"
)

func main() {
	var n int
	var s string
	fmt.Scan(&n)
	for i := 0; i < n; i++ {
		if i > 0 && i < n {
			s += "that "
		}
		if i%2 == 0 {
			s += "I hate "
		} else {
			s += "I love "
		}
	}
	s += "it"
	fmt.Println(s)
}
