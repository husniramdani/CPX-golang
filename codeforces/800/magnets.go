package main

import (
	"bufio"
	"fmt"
	"os"
)

func main() {
	reader := bufio.NewScanner(os.Stdin)
	var n int
	fmt.Scanf("%d", &n)
	var start string
	res := 0
	for reader.Scan() {
		cur := reader.Text()
		if cur != start {
			start = cur
			res += 1
		}
	}
	fmt.Println(res)
}
