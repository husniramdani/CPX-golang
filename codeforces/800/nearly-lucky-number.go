package main

import (
	"fmt"
	"strings"
)

func main() {
	var s string
	fmt.Scan(&s)
	ans := strings.Count(s, "7") + strings.Count(s, "4")
	if ans == 4 || ans == 7 {
		fmt.Println("YES")
	} else {
		fmt.Println("NO")
	}
}
