package main

import "fmt"

func main() {
	var n int
	arr := make(map[int]int)
	fmt.Scan(&n)
	for i := 1; i <= n; i++ {
		var x int
		fmt.Scan(&x)
		arr[x] = i
	}

	for i := 1; i <= n; i++ {
		fmt.Printf("%d ", arr[i])
	}
}
