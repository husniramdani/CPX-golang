package main

import (
	"fmt"
	"strings"
)

func main() {
	var n, m int
	var s string
	fmt.Scan(&n, &m, &s)
	for m > 0 {
		s = strings.ReplaceAll(s, "BG", "GB")
		m--
	}
	fmt.Println(s)
}
