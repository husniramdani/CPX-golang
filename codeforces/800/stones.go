package main

import "fmt"

func main() {
	var n int
	var s string
	ans := 0
	fmt.Scan(&n)
	fmt.Scan(&s)
	for i := 0; i < n-1; i++ {
		if s[i] == s[i+1] {
			ans++
		}
	}
	fmt.Println(ans)
}
