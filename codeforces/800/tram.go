package main

import "fmt"

func main() {
	var n int
	ans := 0
	sisa := 0
	fmt.Scan(&n)
	for ; n > 0; n-- {
		var x, y int
		fmt.Scan(&x, &y)
		sisa -= x
		sisa += y
		if sisa > ans {
			ans = sisa
		}
	}
	fmt.Println(ans)
}
