package main

import "fmt"

func reverse(s string) (r string) {
	for _, v := range s {
		r = string(v) + r
	}
	return
}
func main() {
	var x, y string
	fmt.Scan(&x, &y)
	if x == reverse(y) {
		fmt.Println("YES")
	} else {
		fmt.Println("NO")
	}
}
