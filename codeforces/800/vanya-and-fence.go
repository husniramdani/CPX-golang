package main

import "fmt"

func main() {
	var n, t, ans int
	fmt.Scan(&n, &t)
	for ans = n; n > 0; n-- {
		var x int
		fmt.Scan(&x)
		if x > t {
			ans++
		}
	}
	fmt.Println(ans)
}
