package main

import (
	"fmt"
	"strings"
	"unicode"
)

func main() {
	var x, y int
	var s string
	fmt.Scan(&s)
	for _, c := range s {
		if unicode.IsUpper(c) {
			x++
		} else {
			y++
		}
	}
	if x > y {
		fmt.Println(strings.ToUpper(s))
	} else {
		fmt.Println(strings.ToLower(s))
	}
}
